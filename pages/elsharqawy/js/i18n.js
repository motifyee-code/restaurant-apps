  PC.detectSystemLang = function detectSystemLang() {
    var langs = Array.isArray(navigator.languages) ? navigator.languages : [navigator.language];
    for (var i = 0; i < langs.length; i++) {
      var l = langs[i];
      if (!l) continue;
      if (String(l).toLowerCase().indexOf("ar") === 0) return "ar";
    }
    return "en";
  };

  PC.t = function t(lang, key) {
    var dict = (PC.I18N && PC.I18N[lang]) || PC.I18N.en;
    return (dict && dict[key]) || (PC.I18N.en && PC.I18N.en[key]) || "";
  };
